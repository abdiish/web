<section class="obj_particular">
    <h3>Objetivos particulares</h3>
    <div class="info_particular">
        <article class="particular_text">
            <p class="fa fa-check"> Favorecer el aprendizaje significativo, activo, crítico y creativo en un ambiente de libertad y confianza
                para los niños.</p>
        </article>
        <article class="particular_text">
            <p class="fa fa-check"> Desarrollar en los niños una conciencia de aprecio, respeto y cuidado por la naturaleza y
                por su medio ambiente.</p>
        </article>
        <article class="particular_text">
            <p class="fa fa-check"> Ejercitar habilidades manuales y cognoscitivas que lleven a los niños a considerar su medio, como un lugar deseable
                y favorable para su futuro.</p>
        </article>
        <article class="particular_text">
            <p class="fa fa-check"> Fomentar, entre otros, valores de auto estima y visión de futuro hacia la consecución de un
                desarrollo psico-social adecuado.</p>
        </article>
        <article class="particular_text">
            <p class="fa fa-check"> Involucrar de manera autogestiva a los padres de familia de manera que sean los pilares
                esenciales que apoyen este proceso de enseñanza - aprendizaje.</p>
        </article>
    </div>
</section>
